# 注释内容
print("学会使用python")
# shift + 3 = #，表示单行注释
# 注释就是解释的内容，不随代码进行运行

# 注意：在# 后边要空一格
#直接写，下边有灰色波浪线，编码不规范，编码规范叫做：PEP8编码规范
# 在py文件的最后一行要空着，也不要有空格，这样符合编码规范

"""
    多行注释：三引号，敲击引号三下，回车
    双引号：shift + 单引号 = 双引号
"""
'''
    单双引号的使用没有区别
'''
# print()：带上括号是函数，可以打印，输出一句话
print("快下课了,不要着急")
# 打印多句话用逗号隔开，注意带上引号(单双引号都一样)
print("人生苦短", "我用Python")
# 输出数字
print(145696321, "这是我银行卡余额，随便花")